    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            java.util.Date dt=d.getDate();
            String s1=a.getText();
            String c= (String)category.getSelectedItem();
            if (dt!=null &&!s1.equals("") && !c.equals("")){
                int amount=Integer.parseInt(s1);
                java.sql.Date date= new java.sql.Date(dt.getTime());
                int categoryId = -1;
                 String query = "SELECT cat_id FROM category_info WHERE category = ?";
                
                 PreparedStatement pstmt = db.DbConnect.c.prepareStatement(query);
            pstmt.setString(1, c);
            ResultSet rs = pstmt.executeQuery();

            while (rs.next()) {
                categoryId = rs.getInt("cat_id");
            }
            System.out.print(categoryId);
                
                
                query = "INSERT INTO spending (sdate, amount, cat_id) VALUES (?, ?, ?)";
                pstmt = db.DbConnect.c.prepareStatement(query);
                pstmt.setDate(1, date);        // Assuming 'date' is a String representing the date
                pstmt.setDouble(2, amount);      // Assuming 'amount' is a double value
                pstmt.setInt(3, categoryId);     // Assuming 'categoryId' is an int value
                pstmt.executeUpdate();

                 JOptionPane.showMessageDialog(null, "Expense Added Successfully");
                 getEntries();
                        
            }else{
                JOptionPane.showMessageDialog(null, "Please Fill the Details");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

