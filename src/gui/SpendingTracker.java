     private void displayCategory(){
         try{
             category.removeAllItems();
             String selectQuery = "SELECT * FROM category_info";
                PreparedStatement pstmt = db.DbConnect.c.prepareStatement(selectQuery);
               ResultSet rs=  pstmt.executeQuery();
             while(rs.next()){
                 category.addItem(rs.getString("category"));
             }
         }catch(Exception ex){
             JOptionPane.showMessageDialog(null, ex);
         }
     }
     private void getEntries(){
         try{
             javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
             int rc=dtm.getRowCount();
             int total=0;
             
             while(rc--!=0){
                 dtm.removeRow(0);
             }
             java.time.LocalDate cd=java.time.LocalDate.now();
             java.time.LocalDate bd=cd.minusDays(20);
             // Convert LocalDate to java.sql.Date
            java.sql.Date sqlCd = java.sql.Date.valueOf(cd);
            java.sql.Date sqlBd = java.sql.Date.valueOf(bd);

            // Prepare and execute the query
            String query = "SELECT s.sid AS sid, s.sdate AS sdate, s.amount AS amount, c.category AS category " +
                           "FROM spending s NATURAL JOIN category_info c " +
                           "WHERE s.sdate <= ? AND s.sdate >= ?";
            PreparedStatement pstmt = db.DbConnect.c.prepareStatement(query);
            pstmt.setDate(1, sqlCd);  // Set the parameter for end date (cd)
            pstmt.setDate(2, sqlBd);  // Set the parameter for start date (bd)
            ResultSet rs = pstmt.executeQuery();

             while(rs.next()){
                 int t=rs.getInt("amount");
                 total+=t;
                 Object o[]={
                     rs.getInt("sid"), rs.getDate("sdate"),rs.getString("category") ,t
                 };
                 dtm.addRow(o);
             }
             totalAmount.setText(total+"");
         }catch(Exception ex){
             JOptionPane.showMessageDialog(null, ex);
         }
     }
